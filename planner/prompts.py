def detect_doc_category(topic: str) -> str:
    """根据规划话题判断应该展示哪类文档菜单。"""
    lower = topic.lower()
    if any(s in lower for s in _TRAVEL_SIGNALS):
        return "travel"
    if any(s in lower for s in _MARKETING_DOC_SIGNALS):
        return "marketing"
    if any(s in lower for s in _PROJECT_SIGNALS):
        return "project"
    return "general"


def build_doc_menu(category: str) -> dict[str, object]:
    """根据话题类别生成 DOC_MENU 映射（用户输入 → doc_type）。"""
    types = DOC_CATEGORY_TYPES.get(category, DOC_CATEGORY_TYPES["general"])
    menu: dict[str, object] = {}
    for i, dt in enumerate(types, 1):
        menu[str(i)] = dt
    menu["全部"] = list(types)
    menu["all"] = list(types)

    _KEYWORD_MAP = {
        "brief": ["brief", "方案"],
        "calendar": ["日历", "排期", "calendar"],
        "timeline": ["时间线", "里程碑", "timeline"],
        "decision": ["决策", "备忘", "decision"],
        "itinerary": ["行程", "itinerary", "行程表"],
        "budget": ["预算", "budget", "花费"],
        "packing": ["打包", "packing", "带什么"],
        "action": ["行动", "action", "todo"],
        "spec": ["spec", "规格", "定义", "scope"],
        "features": ["功能", "优先级", "feature", "p0"],
    }
    for dt in types:
        for kw in _KEYWORD_MAP.get(dt, []):
            if kw not in menu:
                menu[kw] = dt
    return menu


# 保留默认 DOC_MENU 用于向后兼容
