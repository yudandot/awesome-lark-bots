# -*- coding: utf-8 -*-
"""
跨文化策略技能 — 中国-全球语境下的沟通、内容、运营策略差异。

不是文化课本，而是帮 AI 在做规划和内容策略时，
自动考虑不同市场的沟通逻辑差异，避免"用一套方案打全球"的懒惰分析。
"""

from skills import Skill, register

_CROSS_CULTURAL = """
## 跨文化策略框架

用于涉及多区域/多文化受众的规划和内容策略。纯国内项目可跳过。

---

### 1. 沟通语境模型（High/Low Context）

| 维度 | 高语境（中国/日本/韩国） | 低语境（美/德/北欧） |
|------|------------------------|---------------------|
| 信息传递 | 言外之意、暗示、氛围 | 直接说、写清楚、不靠猜 |
| 信任建立 | 关系先行，事后面谈效率高 | 流程先行，文档和合同是信任载体 |
| 反馈方式 | 迂回、给面子、私下说 | 直接、公开、当场说 |
| 决策 | 共识驱动，会前沟通比会上重要 | 会议决策，agenda 驱动 |

操作：
- 对内容策略：中国市场用情感共鸣+圈层认同，欧美市场用清晰价值主张+社会证明
- 对团队协作：跨中国-全球团队的提案要同时准备两个版本的表达方式
- 对社区运营：中国社区强调归属感和仪式感，海外社区强调个体表达和透明度

---

### 2. 平台生态差异

| 场景 | 中国 | 全球 |
|------|------|------|
| 短视频 | 抖音/快手：算法推荐为主，完播率是核心 | TikTok/YouTube Shorts：类似但创作者生态更成熟 |
| 图文社区 | 小红书：搜索+推荐双引擎，实用价值驱动 | Instagram/Pinterest：视觉优先，hashtag 发现 |
| 长内容 | B站：UP主人设+深度内容+弹幕文化 | YouTube：SEO+订阅+算法推荐 |
| 即时通讯 | 微信生态（公众号+小程序+社群+朋友圈） | Discord/Reddit：开放社区，匿名/半匿名 |
| 玩家社区 | QQ群/贴吧/NGA：熟人圈子+层级管理 | Discord/Reddit/Fan Wiki：去中心化+UGC |

操作：不要做"全平台铺开"的规划。每个平台的内容逻辑不同，要针对具体平台设计内容策略。

---

### 3. 本地化 ≠ 翻译

| 层级 | 做什么 | 示例 |
|------|--------|------|
| L0 翻译 | 文字转换 | 把中文文案翻成英文 |
| L1 适配 | 格式/习惯调整 | 日期格式、货币、度量衡 |
| L2 文化化 | 情感共鸣点替换 | 中国用春节，美国用 Thanksgiving |
| L3 原生化 | 从目标文化出发重新创作 | 韩国版用 K-pop 审美重做视觉 |

操作：
- 对内容策略，标注每个市场需要做到 L 几
- L0-L1 可以批量处理，L2-L3 需要本地团队或深度了解
- 如果预算/人力只够做 L0-L1，直接说，不要假装能做 L3

---

### 4. 区域优先级判断

不要平均分配资源。排优先级时考虑：

- **市场规模 × 渗透潜力**：大市场低渗透 > 小市场高渗透
- **内容杠杆率**：一套内容能覆盖多少市场？（中文繁体可覆盖台湾+部分东南亚华人）
- **运营成本**：有没有本地团队？时区差几个小时？沟通成本多高？
- **战略价值**：某些市场不赚钱但有品牌价值（如日本对游戏品牌的背书效应）

---

### 5. 跨文化踩坑清单

规划中涉及多区域时，自检以下常见盲点：

- [ ] 是否假设了"中国有效的在海外也有效"？
- [ ] 是否考虑了时区对协作节奏的影响？
- [ ] 是否区分了"全球统一品牌调性"和"各市场内容策略"？（前者要统一，后者要差异化）
- [ ] KPI 是否需要按市场差异化？（ISFP 市场看完播率而非互动率）
- [ ] 是否有 single point of failure？（一个本地团队走了整个市场就瘫了）

---

### 使用原则

- 纯国内项目 → 跳过此框架
- 涉及 2+ 区域 → 至少做区域优先级判断
- 涉及内容策略 → 标注本地化层级
- 不要写"需要考虑文化差异"这种废话，要具体到"哪个差异影响哪个决策"
"""


class CrossCulturalSkill(Skill):
    name = "cross_cultural"
    description = "跨文化策略 — 中国-全球语境下的沟通、内容、运营差异框架"
    trigger_keywords = [
        "全球", "海外", "国际", "跨区域", "本地化", "global", "international",
        "localization", "韩国", "日本", "东南亚", "欧美",
        "cross-cultural", "cross-regional", "多语言",
    ]
    bot_types = ["planner"]

    def get_context(self, **kwargs) -> str:
        return _CROSS_CULTURAL.strip()

    def should_activate(self, user_text: str, bot_type: str = "", **kwargs) -> bool:
        if bot_type == "planner":
            lower = user_text.lower()
            global_signals = [
                "全球", "海外", "国际", "global", "international",
                "韩国", "日本", "东南亚", "欧美", "locali",
                "cross-region", "cross-cultural", "多区域", "多市场",
                "MBTI", "ISFP", "INFP",
            ]
            if any(s.lower() in lower for s in global_signals):
                return True
        return super().should_activate(user_text, bot_type, **kwargs)


register(CrossCulturalSkill())
