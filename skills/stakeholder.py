# -*- coding: utf-8 -*-
"""
利益相关方对齐技能 — 多方协调、内部提案、跨团队推进的实战框架。

不是通用的"沟通技巧"，而是专门针对：
- 跨团队项目推进（多个决策方、不同 KPI、信息不对称）
- 内部提案包装（同一件事给不同受众讲不同故事）
- 预判阻力并设计绕行路径
"""

from skills import Skill, register

_STAKEHOLDER = """
## 利益相关方对齐工具箱

用于涉及多方协调的规划。如果规划只涉及个人执行，跳过此框架。

---

### 1. 利益地图（Stakeholder Map）

在方案落地前，先画清楚谁会被影响、谁能阻断。

操作：
- 列出所有相关方（人/团队/部门）
- 对每个相关方标注：
  - **立场**：支持 / 中立 / 反对 / 未知
  - **影响力**：高（能否决）/ 中（能延缓）/ 低（被通知即可）
  - **核心关切**：他们最在意什么？（KPI、资源、面子、流程合规）
- 重点关注：高影响力 + 反对/未知 的象限

---

### 2. 提案包装术（Framing）

同一个方案，给不同人讲不同的故事。

| 受众 | 关注点 | 包装角度 |
|------|--------|---------|
| 高管/决策者 | ROI、战略对齐、风险 | 先说结论和数字，再说路径 |
| 执行团队 | 工作量、时间线、职责边界 | 先说"你需要做什么"，再说"为什么" |
| 合作方/外部 | 对他们的好处、合作模式 | 先说"你能得到什么"，再说"我需要什么" |
| 技术团队 | 可行性、架构影响、技术债 | 先说约束和接口，再说业务背景 |

操作：对执行计划中的每个关键步骤，标注"这一步需要谁buy-in？用什么角度说服？"

---

### 3. 阻力预判与绕行

提前识别可能的阻断点，设计替代路径。

常见阻力模式：
- **审批瓶颈**：关键人不在 / 优先级不够 → 预案：降低审批层级、拆成小决策
- **资源争夺**：其他项目抢同一批人 → 预案：明确最小可行投入、找替代资源
- **信息不对称**：某方不了解背景就否决 → 预案：提前同步、用他们的语言重述
- **路径依赖**：既有方式很难改 → 预案：增量替换而非一步到位、先试点再推广
- **面子/政治**：方案触碰了某人的领地 → 预案：让对方成为方案的"共同提出者"

操作：对推荐方案做一次"谁可能说不？为什么？怎么绕？"的检查。

---

### 4. 跨团队协作节奏

多团队项目的推进不是"大家一起做"，而是设计好交接点。

操作：
- 明确哪些环节是并行的（各做各的）、哪些是串行的（A做完B才能开始）
- 每个交接点定义：交付物是什么、谁接、deadline、如果延迟的 fallback
- 会议只开必须对齐的节点，其他用异步（文档/消息）

---

### 使用原则

- 个人项目/solo执行 → 通常不需要这套框架，别硬套
- 涉及 2+ 团队/决策者 → 必须做利益地图
- 涉及预算审批/组织变更 → 必须做阻力预判
- 框架是帮用户想清楚"人的因素"，不是生成一份政治学论文
"""


class StakeholderSkill(Skill):
    name = "stakeholder"
    description = "利益相关方对齐 — 多方协调、内部提案、阻力预判的实战框架"
    trigger_keywords = [
        "团队", "协调", "跨部门", "对齐", "提案", "审批", "推进",
        "stakeholder", "alignment", "buy-in", "cross-team",
        "网易", "TGC", "合作方", "利益", "协作",
    ]
    bot_types = ["planner"]

    def get_context(self, **kwargs) -> str:
        return _STAKEHOLDER.strip()

    def should_activate(self, user_text: str, bot_type: str = "", **kwargs) -> bool:
        if bot_type == "planner":
            lower = user_text.lower()
            multi_party_signals = [
                "团队", "跨", "部门", "协调", "对齐", "推进", "审批",
                "合作", "stakeholder", "team", "cross", "网易", "TGC",
                "Global", "提案", "deck", "brief",
            ]
            if any(s.lower() in lower for s in multi_party_signals):
                return True
        return super().should_activate(user_text, bot_type, **kwargs)


register(StakeholderSkill())
