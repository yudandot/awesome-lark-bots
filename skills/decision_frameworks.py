# -*- coding: utf-8 -*-
"""
决策框架技能 — 为规划/决策场景提供结构化思维工具。

不是教 AI "什么是第一性原理"，而是给它一套可以直接套用的推理模板，
让它在分析问题时有具体的思维路径可走，而不是生成泛泛之论。
"""

from skills import Skill, register

_FRAMEWORKS = """
## 决策框架工具箱

以下框架不是知识点，是你必须在分析中实际使用的思维工具。
每次规划至少显式调用 1-2 个框架，标注你在用哪个。

---

### 1. 第一性原理（First Principles）

拆到最底层再重建。用于：目标定义、方案生成。

操作：
- 把用户的需求拆成不可再分的基本事实
- 去掉所有"因为别人也这么做"的假设
- 从基本事实重新推导方案

检验：如果你的分析换一个行业/公司也完全成立，说明你还在类比层面，没到第一性原理。

---

### 2. 可逆 vs 不可逆决策（Type 1 / Type 2）

区分决策权重。用于：方案评估、执行计划。

- Type 1（不可逆）：大预算投入、公开承诺、组织架构变更 → 慢决策，充分分析
- Type 2（可逆）：功能实验、内容测试、工具选型 → 快决策，先做再说

操作：对每个方案标注"这是 Type 1 还是 Type 2？"
如果是 Type 2，不要花 3 页纸分析，直接给执行计划。

---

### 3. Pre-mortem（事前验尸）

假设方案已经失败，倒推原因。用于：风险评估、方案评估。

操作：
- "假设 6 个月后这个方案彻底失败了，最可能的原因是什么？"
- 列出 2-3 个致命失败原因（不是泛泛的"执行不力"，要具体到人/事/节点）
- 针对每个原因设计预防措施或止损线

---

### 4. 约束理论（Theory of Constraints）

找瓶颈，只优化瓶颈。用于：现状分析、执行计划。

操作：
- 整个链路中，哪一环的产能/速度决定了整体上限？
- 只在瓶颈环节投入资源，非瓶颈环节"够用就行"
- 优化完一个瓶颈后重新识别下一个

检验：如果你建议"全面提升各个环节"，说明你没找到瓶颈。

---

### 5. 机会成本思维（Opportunity Cost）

做 A 意味着不做 B。用于：方案评估、优先级排序。

操作：
- 做这件事的时间/资源，如果投到其他方向，最好的替代结果是什么？
- 对用户当前所有在推进的线程，这件事排第几？为什么？
- 对于solo operator或小团队：一个人的时间是最稀缺资源，选择做什么 = 选择不做什么

---

### 6. MECE 拆解（Mutually Exclusive, Collectively Exhaustive）

不遗漏不重叠地拆分问题。用于：问题定义、现状分析。

操作：
- 把问题空间切成互不重叠的子问题
- 检查是否覆盖了所有可能性
- 如果切不干净，说明切的维度不对，换一个维度重切

---

### 使用原则

- 不是每个框架每次都用。根据问题类型选 1-2 个最相关的。
- 调用框架时要显式标注（如"用 pre-mortem 检验："），让用户知道你的推理路径。
- 框架是工具不是目的。如果套框架反而让分析变啰嗦，跳过框架直接给判断。
- 对 solo operator / 小团队的规划，优先用：机会成本 + Type 1/2 + 约束理论。
"""


class DecisionFrameworksSkill(Skill):
    name = "decision_frameworks"
    description = "结构化决策框架 — 第一性原理、pre-mortem、约束理论等思维工具"
    trigger_keywords = [
        "规划", "决策", "方案", "选择", "评估", "优先级", "取舍",
        "plan", "decision", "strategy", "evaluate", "tradeoff",
        "怎么选", "该不该", "值不值", "风险",
    ]
    bot_types = ["planner"]

    def get_context(self, **kwargs) -> str:
        return _FRAMEWORKS.strip()


register(DecisionFrameworksSkill())
